<Template>
  <div>
    <p>{{counter}}</p>
    <button @click="increment">+</button>
    <button @click="decrement">-</button>
  </div>
</Template>
<script>

export default  {

    data() {
      return{counter: 0}
    },
    methods: {
      increment() {
        this.counter++
      },
      decrement() {
        this.counter--
      },
    }
  }
</script>